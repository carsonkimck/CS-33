/*
 * multFiveEighths - multiplies by 5/8 rounding toward 0.
 *   Should exactly duplicate effect of C expression (x*5/8),
 *   including overflow behavior.
 *   Examples: multFiveEighths(77) = 48
 *             multFiveEighths(-22) = -13
 *             multFiveEighths(1073741824) = 134217728 (overflow)
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 3
 */
int multFiveEighths(int x) {
// first we examine the sign of the integer that is passed in and 
// store it into "getSign", next we multiply x by 5 using shifting operators. We then apply the
// bias in order to account for a negative number and store it into result. The final result is based on whether
// we actually need to add the bias to the final number, based on what getSign contains.   
int getSign, result, times5, finalResult  = 0;
getSign = (!x) | (x>>31);
times5 =  (x << 2) + x;
result = getSign & ((times5 + 7 )>>3);
finalResult =(~getSign  & times5 >> 3) + result;
return finalResult;

}


